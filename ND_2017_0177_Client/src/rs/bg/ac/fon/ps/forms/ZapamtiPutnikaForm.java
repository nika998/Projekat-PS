    private void emptyFields() {
        txtName.setText("");
        txtSurname.setText("");
        txtJmbg.setText("");
        buttonGroup1.clearSelection();

    }

    private String selectGender() {

        String gender = "";

        if (rbMale.isSelected()) {
            gender = "Muski";
        }
        if (rbFemale.isSelected()) {
            gender = "Zenski";
        }
        if (rbChild.isSelected()) {
            gender = "Dete";
        }

        return gender;
    }

    private String validateFields(String name, String surname, String jmbg, String gender) {
        StringBuilder sb = new StringBuilder();

        if (name.isEmpty() || surname.isEmpty() || jmbg.isEmpty() || gender.isEmpty()) {
            sb.append("Morate popuniti sledeća polja:\n");
            if (name.isEmpty()) {
                sb.append("Ime\n");
            }
            if (surname.isEmpty()) {
                sb.append("Prezime\n");
            }
            if (jmbg.isEmpty()) {
                sb.append("JMBG\n");
            }
            if (gender.isEmpty()) {
                sb.append("Pol");
            }
        }
        return sb.toString();
    }
