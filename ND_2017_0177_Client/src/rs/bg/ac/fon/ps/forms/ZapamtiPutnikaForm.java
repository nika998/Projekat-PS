    private void btnSavePassengerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavePassengerActionPerformed

        if (JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite da dodate putnika?", "Potvrdite izbor", JOptionPane.YES_NO_OPTION) == 0) {
            try {
                String name = txtName.getText();
                String surname = txtSurname.getText();
                String jmbg = txtJmbg.getText();
                String gender = selectGender();

                String validationMessage = validateFields(name, surname, jmbg, gender);

                if (!validationMessage.isEmpty()) {
                    JOptionPane.showMessageDialog(this, validationMessage, "Nepotpuni podaci", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                passenger.setName(name);
                passenger.setSurname(surname);
                passenger.setJmbg(jmbg);
                passenger.setGender(gender);

                Response res = Controller.getInstance().savePassenger(passenger);

                if (res.getException() == null) {
                    JOptionPane.showMessageDialog(this, "Sistem je zapamtio putnika.", "Uspešno zapamćen putnik", JOptionPane.INFORMATION_MESSAGE);
                    emptyFields();
                } else {
                    JOptionPane.showMessageDialog(this, res.getException().getMessage(), "Neuspešno pamćenje putnika", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti putnika.", "Greška prilikom pamćenja putnika", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnSavePassengerActionPerformed

